package practice0415;

public class ArrList {
	int empID;
	String nameEmp;
	public ArrList(int empId,String nameEmp){
		this.empID=empId;
		this.nameEmp=nameEmp;
	}
	public int getEmpID() {
		return empID;
	}
	public void setEmpID(int empID) {
		this.empID = empID;
	}
	public String getNameEmp() {
		return nameEmp;
	}
	public void setNameEmp(String nameEmp) {
		this.nameEmp = nameEmp;
	}
	
	
}
